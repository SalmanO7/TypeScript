let grade = 70;

if (grade >= 90) {
    console.log(`Your Grade is A+ Fantastic ${grade}`)
} else if (grade >= 80) {
    console.log(`Your Grade is A Exellent ${grade}`)
} else if (grade >= 70) {
    console.log(`Your Grade is B Good ${grade}`)
} else if (grade >= 50) {
    console.log(`Your Grade is C Poor ${grade}`)
} else if (grade >= 40) {
    console.log(`Your Grade is D Pass ${grade}`)
}
else {
    console.log(`You Are Fail ${grade}`)
}
